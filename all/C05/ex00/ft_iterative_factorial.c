#include <unistd.h>

int	ft_iterative_factorial(int nb)
{
	int	res;
	int	i;

	if (nb < 0)
	{
		return (0);
	}
	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	res = 1;
	i = 2;
	while (i <= nb)
	{
		res *= i;
		i++;
	}
	return (res);
}
