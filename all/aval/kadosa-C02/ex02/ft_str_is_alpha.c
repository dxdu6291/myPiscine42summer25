#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	j;

	j = 0;
	while (str[j] != '\0')
	{
		if (!((str[j] >= 'a' && str[j] <= 'z')
				|| (str[j] >= 'A' && str[j] <= 'Z')))
		{
			return (0);
		}
		j++;
	}
	return (1);
}
