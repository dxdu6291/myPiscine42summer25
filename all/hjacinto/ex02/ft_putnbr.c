#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	if (nb == -2147483648)
		write(1, "2147483648", 11);
	if (nb > 9)
		ft_putnbr(nb / 10);
	write(1, &(char){'0' + (nb % 10)}, 1);
}
